                    <p>The IIITDM Jabalpur – Japan collaboration is an aspect of a wider partnership envisaged between Japan and India. The Institute is being developed and nurtured under India - Japan partnership as a unique Institute of its kind with focus on IT enabled design and manufacturing. Under a joint agreement between the two Governments, Japan, a leader in cutting edge manufacturing technologies is committed to provide support for development of the teaching and research programmes and also to promote the infrastructure of  the  Institute.</p>
                    <p><strong>Mr Junichiro Koizumi</strong>, <strong>the Honourable Prime Minister of Japan</strong>, had visited India from April 28 – 30, 2005. During his meeting with <strong>Dr Manmohan Singh</strong>, <strong>the Honourable Prime Minister of India</strong>, an agreement <i>“to make every effort to collaborate towards the development of the Indian Institute of Information Technology, Design &amp; Manufacturing Jabalpur (IIITDM Jabalpur)”</i> was signed between the two sides. </p>
