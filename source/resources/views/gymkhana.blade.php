                    <p>The Students’ Senate has 2-3 representatives from each class. There are around 40 members in the Student Senate. Various gymkhana clubs that are part of Cultural, Sports and Technical, work under Students gymkhana. Club coordinators and co-coordinators will be selected based on their skill and past performance in the Institute by the members of that club and duly ratified by the Student Senate. All members of the student gymkhana who have their CPI>= 6.0 are eligible to become mentors, coordinators, co-coordinators for various clubs.</p>
                    <p>Student gymkhana is headed by Dean, Students, who is nominated by Board of Governor of the Institute for three years. Dean (Students) chairs all the meetings of the Student Senate and guides student representatives in organizing gymkhana activities throughout the year. Apart from this, three faculty members designated as Sports, Cultural and Technical Counsellors, look after sports, cultural and technical activities respectively and respective major festivals organized by the students’ gymkhana.</p>

